function getCookie(name) {
    let cookieValue = null;
    if (document.cookie && document.cookie !== '') {
        const cookies = document.cookie.split(';');
        for (let i = 0; i < cookies.length; i++) {
            const cookie = cookies[i].trim();
            // Does this cookie string begin with the name we want?
            if (cookie.substring(0, name.length + 1) === (name + '=')) {
                cookieValue = decodeURIComponent(cookie.substring(name.length + 1));
                break;
            }
        }
    }
    return cookieValue;
}


function change_theme() {
    let data = document.location.href.split("/");
    let url = data[0] + "//" + data[2] + "/theme/change/"
    document.querySelector('html').classList.toggle('dark');
    if (document.querySelector('html').classList.contains("dark")) {
        document.querySelector('#button-image').innerHTML = '<path stroke-linecap="round" stroke-linejoin="round" d="M12 3v2.25m6.364.386l-1.591 1.591M21 12h-2.25m-.386 6.364l-1.591-1.591M12 18.75V21m-4.773-4.227l-1.591 1.591M5.25 12H3m4.227-4.773L5.636 5.636M15.75 12a3.75 3.75 0 11-7.5 0 3.75 3.75 0 017.5 0z"></path>'
        localStorage.setItem("theme", "dark")
    } else {
        document.querySelector('#button-image').innerHTML = '<path stroke-linecap="round" stroke-linejoin="round" d="M21.752 15.002A9.718 9.718 0 0118 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 003 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 009.002-5.998z"></path>'
        localStorage.setItem("theme", "white")
    }
    const csrftoken = getCookie('csrftoken');
    return fetch(url, {
        method: "POST",
        headers: {'Content-Type': 'application/json', 'X-CSRFToken': csrftoken},
    });
}